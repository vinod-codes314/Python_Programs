n1=int(input("Enter First number: "))
op=input("Enter operator(+,-,*,**,%,/,//): ")
n2=int(input("Enter Second number: "))
if op=="+":
    print("Answer :",n1+n2)
elif op=="-":
    print("Answer :",n1-n2)
elif op=="*":
    print("Answer :",n1*n2)
elif op=="**":
    print("Answer :",n1**n2)
elif op=="%":
    print("Answer :",n1%n2)
elif op=="/":
    print("Answer :",n1/n2)
elif op=="//":
    print("Answer :",n1//n2)
    